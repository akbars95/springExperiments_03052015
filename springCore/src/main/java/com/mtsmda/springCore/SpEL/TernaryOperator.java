public class TernaryOperator {

    private String check;

    public String getCheck() {
        return check;
    }

    public void setCheck(String check) {
        this.check = check;
    }

    @Override
    public String toString() {
        return "TernaryOperator{" +
                "check='" + check + '\'' +
                '}';
    }
}